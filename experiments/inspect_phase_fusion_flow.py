import argparse
import json
from pathlib import Path

import torch
from hydra.utils import instantiate

from addse.data import AudioStreamingDataLoader
from addse.stft import STFT
from addse.utils import load_hydra_config
from experiments.phase_fusion_layered_compare_500 import LayeredCompareConfig, load_cfg
from experiments.phase_fusion_scheme1 import (
    PreDecoderFusionAdapter,
    build_dset,
    load_phase_cnn,
    run_discrete_branch,
    wrap_to_pi,
)


def tensor_stats(x: torch.Tensor) -> dict:
    x_det = x.detach()
    return {
        "shape": list(x_det.shape),
        "dtype": str(x_det.dtype),
        "min": float(x_det.min().item()),
        "max": float(x_det.max().item()),
        "mean": float(x_det.mean().item()),
    }


def load_lm(cfg_path: str, ckpt_path: str, device: torch.device):
    hydra_cfg, _ = load_hydra_config(cfg_path, overrides=None)
    lm = instantiate(hydra_cfg.lm)
    state = torch.load(ckpt_path, map_location=device)
    lm.load_state_dict(state["state_dict"], strict=False)
    lm = lm.to(device)
    lm.eval()
    for p in lm.parameters():
        p.requires_grad = False
    return lm


def main() -> int:
    parser = argparse.ArgumentParser(description="Inspect phase-fusion data flow and tensor forms.")
    parser.add_argument("--config", default="configs/phase_fusion_layered_compare_500.yaml")
    parser.add_argument("--num-samples", type=int, default=1)
    parser.add_argument("--probe-num-steps", type=int, default=8)
    parser.add_argument("--output", default="experiments/phase_fusion_layered_v3/reports/flow_probe.json")
    args = parser.parse_args()

    cfg: LayeredCompareConfig = load_cfg(args.config)
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

    lm = load_lm(cfg.layered_cfg, cfg.layered_ckpt, device)
    print(f"Loaded layered LM on {device}, probe_num_steps={args.probe_num_steps}")
    stft = STFT(frame_length=cfg.frame_length, hop_length=cfg.hop_length, n_fft=cfg.n_fft).to(device)

    phase_bins = cfg.n_fft // 2 + 1
    in_cont_ch = lm.nac.encoder.out_conv.conv.out_channels

    class _PhaseCfg:
        pass

    phase_cfg = _PhaseCfg()
    phase_cfg.phase_cnn_ckpt = cfg.phase_cnn_ckpt
    phase_cnn = load_phase_cnn(phase_cfg, in_ch=in_cont_ch, out_ch=phase_bins, device=device)

    adapter = PreDecoderFusionAdapter(
        in_channels=2 * phase_bins,
        emb_channels=lm.nac.decoder.in_conv.conv.in_channels,
        hidden=cfg.adapter_hidden,
        use_dilation=cfg.use_dilation,
        dilation_rates=cfg.dilation_rates,
    ).to(device)
    init_state = torch.load(cfg.init_adapter_ckpt, map_location=device)
    adapter.load_state_dict(init_state["model"], strict=True)
    adapter.eval()
    for p in adapter.parameters():
        p.requires_grad = False

    dset = build_dset(cfg, length=max(1, int(args.num_samples)), reset_rngs=True)
    loader = AudioStreamingDataLoader(dset, batch_size=1, num_workers=0, shuffle=False)
    noisy, clean, _ = next(iter(loader))
    noisy = noisy.to(device)
    clean = clean.to(device)

    with torch.no_grad():
        print("Running discrete branch solve for probe...")
        n_pad = (lm.nac.downsampling_factor - noisy.shape[-1]) % lm.nac.downsampling_factor
        x_pad = torch.nn.functional.pad(noisy, (0, n_pad))

        x_tok, x_q = lm.nac.encode(x_pad, no_sum=True, domain="q")
        _, z_noisy = lm.nac.encode(x_pad, domain="x")

        y_tok = lm.solve(x_tok, x_q, max(1, int(args.probe_num_steps)))
        y_q_books = lm.nac.quantizer.decode(y_tok, output_no_sum=True, domain="code")
        y_q_sum = y_q_books.sum(dim=2)

        noisy_stft = stft(noisy)
        noisy_phase = torch.angle(noisy_stft[:, 0])
        delta_phase = phase_cnn(z_noisy, target_frames=noisy_phase.shape[-1])
        pred_phase = wrap_to_pi(noisy_phase + delta_phase.clamp(-cfg.phase_delta_clip, cfg.phase_delta_clip))
        phase_feat = torch.cat([torch.cos(pred_phase), torch.sin(pred_phase)], dim=1)

        latent_corr = adapter(phase_feat, latent_frames=y_q_sum.shape[-1])
        y_q_fused = y_q_sum + cfg.fusion_scale_layered * latent_corr

        y_base = lm.nac.decoder(y_q_sum)[..., : clean.shape[-1]]
        y_fused = lm.nac.decoder(y_q_fused)[..., : clean.shape[-1]]

    report = {
        "meta": {
            "device": str(device),
            "snr_range": [cfg.snr_min, cfg.snr_max],
            "segment_length": cfg.segment_length,
            "fusion_scale_layered": cfg.fusion_scale_layered,
            "dilation_rates": list(cfg.dilation_rates),
            "note": "logits are generated by lm.solve before fusion; fusion modifies y_q_sum (float latent) pre-decoder.",
        },
        "tensors": {
            "noisy": tensor_stats(noisy),
            "clean": tensor_stats(clean),
            "x_tok_discrete": tensor_stats(x_tok.float()),
            "x_q_quantized": tensor_stats(x_q),
            "z_noisy_continuous": tensor_stats(z_noisy),
            "y_tok_discrete": tensor_stats(y_tok.float()),
            "y_q_books": tensor_stats(y_q_books),
            "y_q_sum": tensor_stats(y_q_sum),
            "noisy_phase": tensor_stats(noisy_phase),
            "delta_phase_from_cnn": tensor_stats(delta_phase),
            "phase_feat_cos_sin": tensor_stats(phase_feat),
            "latent_corr_from_adapter": tensor_stats(latent_corr),
            "y_q_fused": tensor_stats(y_q_fused),
            "y_base_wave": tensor_stats(y_base),
            "y_fused_wave": tensor_stats(y_fused),
        },
        "dims_check": {
            "num_codebooks_from_tokens": int(y_tok.shape[1]),
            "stft_bins": int(phase_bins),
            "phase_feat_channels": int(phase_feat.shape[1]),
            "decoder_in_channels": int(lm.nac.decoder.in_conv.conv.in_channels),
            "is_phase_feat_channel_eq_2x_bins": bool(phase_feat.shape[1] == 2 * phase_bins),
            "is_latent_corr_channel_eq_decoder_in": bool(latent_corr.shape[1] == lm.nac.decoder.in_conv.conv.in_channels),
        },
        "trainable_params": {
            "lm_trainable": int(sum(p.numel() for p in lm.parameters() if p.requires_grad)),
            "phase_cnn_trainable": int(sum(p.numel() for p in phase_cnn.parameters() if p.requires_grad)),
            "adapter_trainable": int(sum(p.numel() for p in adapter.parameters() if p.requires_grad)),
        },
    }

    out_path = Path(args.output)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with out_path.open("w", encoding="utf-8") as f:
        json.dump(report, f, ensure_ascii=False, indent=2)

    print("=== FLOW PROBE SUMMARY ===")
    print(json.dumps(report["meta"], ensure_ascii=False, indent=2))
    print(json.dumps(report["dims_check"], ensure_ascii=False, indent=2))
    print(json.dumps(report["trainable_params"], ensure_ascii=False, indent=2))
    print(f"Saved: {out_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
